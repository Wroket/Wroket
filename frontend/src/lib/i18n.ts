export type TranslationKey = keyof typeof translations;

let currentLocale: Locale = "fr";

export function setLocale(locale: Locale): void {
  currentLocale = locale;
  if (typeof window !== "undefined") {
    localStorage.setItem("wroket-locale", locale);
  }
}

export function getLocale(): Locale {
  return currentLocale;
}

export function initLocale(): Locale {
  if (typeof window !== "undefined") {
    const stored = localStorage.getItem("wroket-locale");
    if (stored === "fr" || stored === "en") {
      currentLocale = stored;
    }
  }
  return currentLocale;
}

export function t(key: TranslationKey | (string & {})): string {
  const entry = (translations as Record<string, Record<Locale, string>>)[key];
  if (!entry) return key;
  return entry[currentLocale] ?? entry.fr;
}
